#- Como utilizar

# O Python possui seta operadores arimetricos que são utilizados para realizar operações matemáticas como adição, subtração e multiplicação . São elas:

# + Adição                          soma os dois valores                                                                                   X + Y
# - Subtração                       Subtrai o valor da esquerda para a direita                                                             X - Y
# * Multiplicação                   Multilplica os dois valores                                                                            X * Y
# / Divisão                         Divide o valor da esquerda pelo valor da direita. O resultado sempre retorna como Float                X / Y
# % Módulo                          Resto da divisão do valor da esquerda pelo o da direita. o resultado sempre retorna int                X % Y
# // Divisão inteira                Divisão inteira do valor da esquerda pelo valor da direita. O resultado sempre int                     X // Y
# ** Exponenciação                  Realiza a pontencialização do valor da esquerda, elevando ele ao valor da direita                      X ** Y   

# Cada operador tem sua utilzação dentro do necessario, no Python quando for calcular algum valor ele usa a convenção tradicional da matemática muito conhecido como PEMDAS
# LETRA:                 SIGNIFICADO:
# P                     Parenteses
# E                     Exponenciação
# M                     Multiplicação
# D                     Divisão
# A                     Adição

